// every loof have three part such as initial or starting part, ending part, update part.

// forward while loof
var i = 0; //initial or starting part

while(i <=10) //ending part
{
    i++;    // update part
    console.log(i)
}

var mylovingYrs = 0;

while(mylovingYrs < 6){
    console.log('We are loving together for 6 years.')
    mylovingYrs++;
    console.log(mylovingYrs);
}
console.log('The End');

// backward while loof
var x = 10;

while(x > 0){
    x--;
    console.log(x);
}

console.log('Ends here')

var myJobYrs = 7;

while(myJobYrs > 0){
    console.log('My job experience is 7 years.');
    myJobYrs--;
    console.log(myJobYrs);

}

// print even number from 1 to 50 by using while loof
var evenNumber = 0;

while(evenNumber <=50){
    evenNumber += 2;
    console.log('The even number of ' + evenNumber +' from 2-50.')
}

console.log('The even number end')

// print odd number from 1 to 50 by using while loof

var oddNumber = 0;

while(oddNumber < 50){
    oddNumber += 3;
    console.log('The odd number '+ oddNumber + ' from 1 - 50');
}