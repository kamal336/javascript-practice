// every loof have three part such as initial or starting part,condition or ending part, update part.

// forward for loof

for(var i = 0; i <= 10; i++){
    console.log(i);

}

for(var myLove = 14; myLove <=21; myLove++){
    console.log('I love naim from 6 years ago until now.Loving year 20'+myLove);
}

console.log('forward for loof end');

// backward for loof

for(x = 10; x > 0; x--){
    console.log('Backward for loof '+x)
}

for(var loveUntil = 21; loveUntil >= 14; loveUntil--){
    console.log('Love alive now from 20'+loveUntil);
}

// print even number from 1 - 50 by using for loof

for(var evenNumber = 2; evenNumber < 50; evenNumber += 2){
    console.log('The even number is '+ evenNumber + ' from 1 - 50');
}

console.log('The even number end')

