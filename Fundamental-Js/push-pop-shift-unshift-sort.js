// adding a Element by push method
var names = ["Naim", "Koli", "Dewan","kamol"];

console.log(names); 

names.push("akter");
names.push('kamal');
console.log(names);

// remove a element by pop method
var ages = [13,34,54,22];

console.log(ages);

ages.pop();
console.log(ages);

ages.pop();
console.log(ages);

ages.pop();
console.log(ages);

// add a Element from first by using unshift method
var koreanFriends = ['alam', 'mizan', 'sunny','abdullah'];

console.log(koreanFriends);

koreanFriends.unshift('iman');
console.log(koreanFriends);

koreanFriends.unshift('wasim');
console.log(koreanFriends);


// remove an Element from first using shift method

var childhoodFriends = ['akter','kamal','tyab','anwar'];

console.log(childhoodFriends);

childhoodFriends.shift();
console.log(childhoodFriends);

childhoodFriends.shift();
console.log(childhoodFriends);

childhoodFriends.shift();
console.log(childhoodFriends);

